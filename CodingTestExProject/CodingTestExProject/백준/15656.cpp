/*
15656 N과 M (7)
N개의 자연수와 자연수 M이 주어졌을 때, 아래 조건을 만족하는 길이가 M인 수열을 모두 구하는 프로그램을 작성하시오. N개의 자연수는 모두 다른 수이다.

N개의 자연수 중에서 M개를 고른 수열
같은 수를 여러 번 골라도 된다.

한 줄에 하나씩 문제의 조건을 만족하는 수열을 출력한다. 중복되는 수열을 여러 번 출력하면 안되며, 각 수열은 공백으로 구분해서 출력해야 한다.
수열은 사전 순으로 증가하는 순서로 출력해야 한다.

이전 문제에서 visited로 방문 표시만 하지않으면 되는 문제이다.
*/
#include <iostream>
#include <algorithm>
using namespace std;

int N, M;
int arr[8] = { 0, };
int selected[8] = { 0, };

void bf(int idx)
{
	if (idx == M)
	{
		for (int i = 0; i < M; i++)
			cout << selected[i] << " ";
		cout << "\n";
		return;
	}
	for (int i = 0; i < N; i++)
	{
		selected[idx] = arr[i];
		bf(idx + 1);
	}
}

int main() {
	cin >> N >> M;
	for (int i = 0; i < N; i++)
		cin >> arr[i];
	sort(arr, arr + N);
	bf(0);
	return 0;
}