/*
9465 스티커

상근이의 여동생 상냥이는 문방구에서 스티커 2n개를 구매했다. 스티커는 그림 (a)와 같이 2행 n열로 배치되어 있다. 상냥이는 스티커를 이용해 책상을 꾸미려고 한다.

상냥이가 구매한 스티커의 품질은 매우 좋지 않다. 스티커 한 장을 떼면, 그 스티커와 변을 공유하는 스티커는 모두 찢어져서 사용할 수 없게 된다. 즉, 뗀 스티커의 왼쪽, 오른쪽, 위, 아래에 있는 스티커는 사용할 수 없게 된다.
모든 스티커를 붙일 수 없게된 상냥이는 각 스티커에 점수를 매기고, 점수의 합이 최대가 되게 스티커를 떼어내려고 한다. 먼저, 그림 (b)와 같이 각 스티커에 점수를 매겼다. 상냥이가 뗄 수 있는 스티커의 점수의 최댓값을 구하는 프로그램을 작성하시오. 즉, 2n개의 스티커 중에서 점수의 합이 최대가 되면서 서로 변을 공유 하지 않는 스티커 집합을 구해야 한다.

위의 그림의 경우에 점수가 50, 50, 100, 60인 스티커를 고르면, 점수는 260이 되고 이 것이 최대 점수이다. 가장 높은 점수를 가지는 두 스티커 (100과 70)은 변을 공유하기 때문에, 동시에 뗄 수 없다.

이차원 배열을 이용한 DP 문제로 각 스텝마다 최대값을 구할 수 있는 경우의 수를 생각하면된다.
2단계전의 1행,2행 중 하나를 고르거나
1단계전의 현재 행의 반대를 고르는 3가지 경우 중 max값을 찾아 저장하면 된다.
dp를 모두 완료하고 두행의 가장 끝 값중 최대값이 답이된다.

*/
#include <iostream>
#include <algorithm>
#include <cstring>
using namespace std;

int T, N;
int arr[2][100001];
int dp[2][100001];
int ans = 0;

void ChangeArr() {
	memset(arr, 0, sizeof(arr));
	memset(dp, 0, sizeof(dp));
	for (int i = 0; i < 2; i++)
	{
		for (int j = 0; j < N; j++)
		{
			cin >> arr[i][j];
		}
	}
}

int main() {
	cin >> T;
	for (int i = 0; i < T; i++) {
		cin >> N;
		ChangeArr();
		dp[0][0] = arr[0][0]; dp[1][0] = arr[1][0];
		for (int i = 1; i < N; i++)
		{
			dp[0][i] = max(dp[1][i - 1] + arr[0][i], max(dp[0][i - 2] + arr[0][i], dp[1][i - 2] + arr[0][i]));
			dp[1][i] = max(dp[0][i - 1] + arr[1][i], max(dp[0][i - 2] + arr[1][i], dp[1][i - 2] + arr[1][i]));
		}
		ans = max(dp[0][N - 1], dp[1][N - 1]);
		cout << ans << endl;
	}
	return 0;
}